char	*ft_rot42(char *str)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (str[j] != '\0')
	{
		i = 0;
		while (i != 42)
		{
			if ((str[j] >= 97 && str[j] <= 122) || (str[j] >= 65 && str[j] <=
						95))
			{
				if (str[j] == 'z' || str[j] == 'Z')
					str[j] = str[j] - 26;
				str[j] = str[j] + 1;
			}
			i++;
		}
		j++;
	}
	return (str);
}
