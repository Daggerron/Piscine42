int			ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int result;

	result = 0;
	i = 0;
	while (tab[i])
	{
		if (f(tab[i]) == 1)
			result += 1;
		i++;
	}
	return (result);
}
